package inheritance;

public class InheritanceDocs {
	/*
	 * ============= Inheritance =============
	 * # code re-usability
	 * # process of accessing properties and methods of parent/super/base class to 
	 * 	 child/sub/derived class is known as inheritance.
	 * 
	 * # also known as IS-A relationship
	 * # its a generalization to specialization process.
	 * # we can't inherit private properties and methods from parent class.
	 * # we can inherit static props. and methods.
	 * # Constructors are not inherited.
	 * # 'super' keyword is used to represent parent object in case both names are same.
	 * 		we can access properties, methods and constructors of parent class to child class through 'super' keyword.
	 * # we can inherit only one class at a time.
	 * 
	 * 
	 * syntax :
	 * 
	 * 	class child_class_name  extends  parent_class_name{
	 * 
	 * 		// properties : parent props. + child props.
	 * 
	 * 		// methods	: parent methods + child methods
	 * 	}
	 * 
	 * e.g.
	 * 
	 * 		class A{		
	 * 
	 * 		}
	 * 
	 * 		class B extends A{
	 * 
	 * 		}
	 * 
	 * 
	 * 
	 * 
	 * 
	 */

}
