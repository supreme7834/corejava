package methodoverriding;

public class MethodOverridingDocs {
	/*
	 * ==================== Method Overriding ===================
	 * 
	 * # used to achieve polymorphism (method overloading/overriding)
	 * # to implement method overriding there must be IS-A relationship(inheritance)
	 * # method name, return type and list of arguments same as parent class method
	 * 	 but implementation should be different in child class method.
	 * 
	 * # inherit means : child class uses methods and properties of parent class.
	 * # overriding : child class modifies/edits/changes parent class methods.
	 * 
	 * e.g.
	 * 
	 * 	class A {
	 * 
	 * 		void run(){
	 * 				sysout(4000);
	 * 			}
	 * 		}
	 * 
	 * 	class B extends A {
	 * 
	 * 		@Override
	 * 		void run(){
	 * 				sysout(15000);
	 * 			}
	 * 		}
	 * 
	 * 
	 * 
	 * 	
	 */

}
